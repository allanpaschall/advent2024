def horizontalToVertical(lines):
	width = len(lines[0])
	newlines = ["" for i in range(width)]

	# Loop through each string and append corresponding characters to the new strings
	for line in lines:
		for i in range(width):
			newlines[i] += line[i]

	return newlines

def search_diagonal(grid, word):
	rows = len(grid)
	cols = len(grid[0])
	word_len = len(word)
	
	# Helper function to search diagonals in all directions
	def check_diagonal(x, y, dx, dy):
		word_found = []
		for i in range(word_len):
			nx, ny = x + i * dx, y + i * dy
			if 0 <= nx < rows and 0 <= ny < cols and grid[nx][ny] == word[i]:
				word_found.append((nx, ny))
			else:
				return None
		return word_found
	
	found_words = []
	
	# Iterate over every position in the grid to find diagonals
	for i in range(rows):
		for j in range(cols):
			# Check all four diagonal directions
			for dx, dy in [(-1, -1), (-1, 1), (1, -1), (1, 1)]:
				result = check_diagonal(i, j, dx, dy)
				if result:
					found_words.append(result)
					
	return found_words

